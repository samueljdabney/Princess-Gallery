function renderCarousel(e){
    var Ind = null;
    var nav_item = document.querySelectorAll('[nav-item-index]');
    if(e.target){
       for(let navI in nav_item){
            let navElm = nav_item[navI];
            if(navElm && navElm.parentNode && navElm.contains(e.target)){
                var attr_value = navElm.getAttribute('nav-item-index');
                attr_value = attr_value && parseInt(attr_value);
                Ind = attr_value;
            }
        } 
       
    }else{
        Ind = e; 
    }
    
    var carouselItems = document.getElementById('carousel-items');
    while(carouselItems.firstChild){
        carouselItems.removeChild(carouselItems.firstChild);
    }
  
    
        data[Ind].pictures.map((s,i)=>{
            var div2 = document.createElement('div');
            div2.classList.add('carousel-item');
            if(i===0){div2.classList.add('active');}
            var img = document.createElement('img');
            img.classList.add('d-block');
            img.classList.add('w-100');
            img.classList.add('slideImage');
            img.src=s;
            div2.appendChild(img);
            carouselItems.appendChild(div2);

        });
    
    
    
    console.log('the click is working');
}



function init(){
    var nav_item = document.querySelectorAll('[nav-item-index]');
     for(let navI in nav_item){
        let navElm = nav_item[navI];
        if(navElm && navElm.parentNode){
            navElm.addEventListener('click',renderCarousel);
        }
    }
    renderCarousel(0);
}



